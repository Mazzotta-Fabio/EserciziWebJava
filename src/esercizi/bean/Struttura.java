package esercizi.bean;

public class Struttura {
	private String albergo;
	private String camere;
	private String posizione;
	private String servizi;
	private String pulizia;
	
	public Struttura() {}

	public String getAlbergo() {
		return albergo;
	}

	public void setAlbergo(String albergo) {
		this.albergo = albergo;
	}

	public String getCamere() {
		return camere;
	}

	public void setCamere(String camere) {
		this.camere = camere;
	}

	public String getPosizione() {
		return posizione;
	}

	public void setPosizione(String posizione) {
		this.posizione = posizione;
	}

	public String getServizi() {
		return servizi;
	}

	public void setServizi(String servizi) {
		this.servizi = servizi;
	}

	public String getPulizia() {
		return pulizia;
	}

	public void setPulizia(String pulizia) {
		this.pulizia = pulizia;
	}
	
	
}
